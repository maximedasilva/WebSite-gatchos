                        <h4 style="Line-Height: 1.7;">L’Association a été créée par des parents soucieux d’offrir au plus grand nombre, la possibilité d’apprendre la musique sans que la contrainte financière soit un obstacle.

L’école de musique accueille des enfants à partir de trois ans. Evidemment, ils sont trop jeunes encore pour apprendre la musique, mais ils découvrent l’univers musical par le biais de la chanson. Ils découvrent aussi la musique par l’apprentissage du solfège.
                        </h4><h4 style="Line-Height: 1.7;">
