La Musicale des Gaves a choisi de développer au maximum, la musique de groupe. Très vite, dans son apprentissage, l’enfant intègrera l’orchestre de l’école de musique. Il découvrira avec bonheur ses premières émotions de musicien d’orchestre.</h4 >
