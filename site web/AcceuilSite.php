                        <h3>Les cours d'instruments</h3>
                        <h4 style="Line-Height: 1.7;">Ils sont dispensés par nos professeurs, à raison d’une demie- heure par semaine en cours individuels. En février, les élèves présentent devant l’ensemble des professeurs, lors des auditions, le travail des cinq premiers mois. Une évaluation de leur progression est alors faite et des orientations de travail sont données à chaque élève. En fin d’année, les élèves de l’école de musique passent un examen, devant un jury extérieur à l’Association. Le Jury note musicalité, la justesse du son, les rythmes. Cette note est prise en compte avec l’épreuve de solfège et la note des auditions pour la moyenne de l’année musicale.</h4>

